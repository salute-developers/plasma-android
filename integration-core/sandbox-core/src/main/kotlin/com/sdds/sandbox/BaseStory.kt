package com.sdds.sandbox

import kotlin.reflect.KClass

/**
 * Базовый интерфейс для всех историй компонентов в сторибуке.
 * Определяет основные компоненты, необходимые для отображения и управления состоянием истории.
 *
 * @param State тип состояния пользовательского интерфейса, с которым работает история
 */
interface BaseStory<State : UiState> {
    /**
     * Уникальный ключ компонента, используемый для идентификации истории
     * в системе навигации и управления состоянием.
     */
    val component: ComponentKey

    /**
     * Начальное состояние истории, которое используется при первом отображении
     * или при сбросе текущего состояния.
     */
    val defaultState: State

    /**
     * Провайдер свойств, отвечающий за генерацию доступных для настройки
     * параметров состояния в панели управления сторибука.
     */
    val propertiesProducer: PropertiesProducer<State>

    /**
     * Трансформер состояния, обрабатывающий изменения состояния
     * на основе выбранных пользователем свойств в панели управления.
     */
    val stateTransformer: StateTransformer<State>
}

/**
 * Аннотация для пометки класса как истории компонента.
 * Истории определяют различные состояния и вариации отображения UI-компонентов
 * для тестирования и документирования в сторибуке.
 *
 * Применяется к классам.
 */
@Target(allowedTargets = [AnnotationTarget.CLASS])
@Retention
annotation class Story

/**
 * Аннотация для пометки класса данных как состояния UI для истории.
 * Позволяет системе сторибука идентифицировать классы, представляющие
 * состояние пользовательского интерфейса компонента.
 *
 * Применяется к классам.
 */
@Target(allowedTargets = [AnnotationTarget.CLASS])
@Retention
annotation class StoryUiState

/**
 * Аннотация для пометки свойства как настраиваемого параметра истории.
 * Такие свойства автоматически отображаются в панели управления сторибука,
 * позволяя динамически изменять состояние компонента.
 *
 * @param displayName пользовательское отображаемое имя свойства в интерфейсе.
 * Если не указано, используется имя поля.
 * @param producedBy класс провайдера, отвечающий за генерацию возможных значений
 * для данного свойства. По умолчанию используется [Unit], что означает
 * отсутствие специального провайдера.
 * @param transformedBy класс трансформера, обрабатывающий изменения значения свойства
 * и применяющий их к состоянию. По умолчанию используется [Unit],
 * что означает отсутствие специального трансформера.
 *
 * Применяется к полям и свойствам класса.
 */
@Target(allowedTargets = [AnnotationTarget.FIELD, AnnotationTarget.PROPERTY])
@Retention
annotation class StoryProperty(
    val displayName: String = "",
    val producedBy: KClass<*> = Unit::class,
    val transformedBy: KClass<*> = Unit::class,
)
