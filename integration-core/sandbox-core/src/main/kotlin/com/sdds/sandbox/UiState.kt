package com.sdds.sandbox

/**
 * Базовый интерфейс для всех состояний пользовательского интерфейса в сторибуке.
 *
 * Определяет минимальный контракт для состояний компонентов, включая поддержку
 * вариаций и внешнего вида, а также механизмы их обновления. Состояния,
 * реализующие этот интерфейс, автоматически интегрируются с системой
 * свойств сторибука через аннотацию [StoryProperty].
 *
 * Состояние компонента с вариацией [variant] и внешним видом [appearance].
 * Эти поля помечены как настраиваемые свойства и будут автоматически
 * отображаться в панели управления сторибука.
 */
interface UiState {

    /**
     * Вариация компонента, определяющая его внутреннее поведение или состояние.
     *
     * Например, для кнопки это могут быть значения: "enabled", "disabled", "loading".
     * Помечена [StoryProperty], что позволяет динамически изменять вариацию
     * через интерфейс сторибука.
     *
     * По умолчанию возвращает пустую строку, что означает отсутствие конкретной вариации.
     */
    @StoryProperty
    val variant: String
        get() = ""

    /**
     * Внешний вид компонента, определяющий его стилизацию и оформление.
     *
     * Например, для кнопки это могут быть значения: "primary", "secondary", "outline".
     * Помечена [StoryProperty], что позволяет динамически переключать внешний вид
     * через интерфейс сторибука.
     *
     * По умолчанию возвращает пустую строку, что означает использование стиля по умолчанию.
     */
    @StoryProperty
    val appearance: String
        get() = ""

    /**
     * Создает копию состояния с обновленными значениями внешнего вида и вариации.
     *
     * Используется при изменении соответствующих свойств в панели управления
     * для создания нового состояния без мутации существующего.
     *
     * @param appearance новое значение внешнего вида компонента
     * @param variant новое значение вариации компонента
     * @return новый экземпляр состояния с примененными изменениями
     */
    fun updateVariant(appearance: String, variant: String): UiState
}

/**
 * Трансформер состояния, обрабатывающий изменения отдельных свойств.
 *
 * Отвечает за применение изменений конкретного свойства к состоянию компонента.
 * Используется системой сторибука для обновления состояния при взаимодействии
 * пользователя с элементами управления в панели свойств.
 *
 * @param State тип состояния, должен реализовывать [UiState]
 */
interface StateTransformer<State : UiState> {

    /**
     * Преобразует состояние, применяя изменение указанного свойства.
     *
     * @param state текущее состояние компонента
     * @param name имя изменяемого свойства (соответствует полю в классе состояния)
     * @param value новое значение свойства
     * @return новое состояние с примененным изменением
     */
    fun transform(state: State, name: String, value: Any): State
}

/**
 * Трансформер для преобразования значений между различными типами.
 *
 * Используется, когда значение свойства в UI должно быть преобразовано
 * перед применением к состоянию (например, String в Int или Boolean во что-то другое).
 *
 * @param Input тип входного значения (обычно из UI-контролов)
 * @param Output тип выходного значения, ожидаемый состоянием
 */
interface StateValueTransformer<Input, Output> {

    /**
     * Преобразует входное значение в выходной тип.
     *
     * @param input исходное значение
     * @return преобразованное значение
     */
    fun transform(input: Input): Output
}

/**
 * Создает провайдер свойств по умолчанию для состояния.
 *
 * Возвращает пустой список свойств. Предназначен для переопределения
 * в конкретных реализациях [UiState] с использованием reflection или
 * других механизмов для автоматического сбора свойств, помеченных [StoryProperty].
 *
 * @param State тип состояния, для которого создается провайдер
 * @return [PropertiesProducer] возвращающий пустой список свойств
 *
 * @see PropertiesProducer
 * @see StoryProperty
 */
fun <State : UiState> UiState.producer(): PropertiesProducer<State> = object : PropertiesProducer<State> {
    override fun getProperties(state: State): List<Property<*>> {
        return emptyList()
    }
}

/**
 * Создает трансформер состояния по умолчанию.
 *
 * Возвращает трансформер, который не изменяет состояние.
 * Предназначен для переопределения в конкретных реализациях [UiState]
 * для обеспечения корректной обработки изменений свойств.
 *
 * @param State тип состояния, для которого создается трансформер
 * @return [StateTransformer] который возвращает исходное состояние без изменений
 *
 * @see StateTransformer
 */
fun <State : UiState> UiState.transformer(): StateTransformer<State> = object : StateTransformer<State> {
    override fun transform(state: State, name: String, value: Any): State {
        return state
    }
}
