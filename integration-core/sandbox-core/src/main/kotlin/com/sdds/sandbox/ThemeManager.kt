package com.sdds.sandbox

import kotlinx.coroutines.flow.MutableStateFlow
import kotlinx.coroutines.flow.StateFlow
import kotlinx.coroutines.flow.asStateFlow

/**
 * Глобальный менеджер тем, управляющий текущей темой оформления в сторибуке.
 *
 * Предоставляет централизованное хранилище текущей темы и механизмы для её
 * обновления. Используется всеми компонентами сторибука для получения актуальной
 * информации о стилевом оформлении. Реализован как синглтон для обеспечения
 * единого источника истины о состоянии темы во всём приложении.
 *
 * Особенности:
 * - Потокобезопасное хранение текущей темы через [StateFlow]
 * - Возможность подписки на изменения темы
 * - Изначально инициализируется темой по умолчанию [Theme.Default]
 *
 * @see Theme
 * @see StateFlow
 *
 * @author Малышев Александр on 04.03.2025
 */
object ThemeManager {

    /**
     * Внутреннее хранилище текущей темы в виде изменяемого потока данных.
     * Инициализируется темой по умолчанию.
     */
    private var _currentTheme = MutableStateFlow(Theme.Default)

    /**
     * Публичный поток текущей темы для наблюдения за изменениями.
     *
     * Позволяет компонентам подписываться на обновления темы и реагировать
     * на их изменения, например, для перерисовки UI в соответствии с новой темой.
     *
     * @return [StateFlow] эмитирующий текущую тему при каждом её изменении
     */
    val currentTheme: StateFlow<Theme>
        get() = _currentTheme.asStateFlow()

    /**
     * Обновляет текущую тему на новую.
     *
     * Вызов этой функции приводит к обновлению значения в [_currentTheme]
     * и уведомлению всех подписчиков [currentTheme] об изменении.
     * Используется для динамического переключения тем в рантайме.
     *
     * @param theme новая тема для применения
     *
     * @see Theme
     */
    fun updateTheme(theme: Theme) {
        _currentTheme.value = theme
    }
}
