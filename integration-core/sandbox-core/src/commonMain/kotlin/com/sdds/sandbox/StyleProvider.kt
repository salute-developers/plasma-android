package com.sdds.sandbox

/**
 * Ссылка на стиль компонента, обеспечивающая отложенное получение конкретного экземпляра стиля.
 *
 * Позволяет передавать и хранить стили без их немедленного создания, что особенно полезно
 * в Compose-окружении, где создание стиля может зависеть от композиционного контекста.
 *
 * @param S тип стиля, к которому предоставляется ссылка (ковариантный)
 */
interface StyleReference<out S> {

    /**
     * Возвращает конкретный экземпляр стиля.
     *
     * Вызов этой функции создаёт или возвращает существующий экземпляр стиля.
     * В Compose-реализациях обычно является композируемой функцией.
     *
     * @return экземпляр стиля типа [S]
     */
    fun get(): S
}

/**
 * Абстрактный провайдер стилей для компонента, управляющий различными вариациями оформления.
 *
 * Обеспечивает централизованное хранение и доступ ко всем стилевым вариациям компонента.
 * Каждая вариация идентифицируется строковым ключом и связана с соответствующей [StyleReference].
 * Провайдер также определяет вариацию по умолчанию для случаев, когда конкретная не указана.
 *
 * @param SR тип ссылки на стиль, должен реализовывать [StyleReference]
 *
 * @see StyleReference
 * @see ComposeStyleProvider
 */
abstract class StyleProvider<SR : StyleReference<*>> {

    /**
     * Коллекция всех доступных вариаций стилей компонента.
     *
     * Ключом является строковый идентификатор вариации (например, "primary", "secondary"),
     * значением - соответствующая ссылка на стиль [SR]. Должна быть определена в наследниках.
     */
    protected abstract val variations: Map<String, SR>

    /**
     * Список всех доступных идентификаторов вариаций компонента.
     *
     * Используется для построения UI выбора стиля в панели управления сторибука.
     *
     * @return список строковых ключей всех зарегистрированных вариаций
     */
    val variants: List<String>
        get() = variations.keys.toList()

    /**
     * Вариация стиля по умолчанию.
     *
     * Возвращает ключ первой вариации из [variations]. Используется, когда
     * конкретная вариация не указана или запрошенная не найдена.
     *
     * @return строковый ключ вариации по умолчанию
     *
     * @throws NoSuchElementException если [variations] пуст
     */
    open val defaultVariant: String
        get() = variations.entries.first().key

    /**
     * Возвращает ссылку на стиль для указанной вариации.
     *
     * Сначала пытается найти вариацию по переданному ключу. Если такой вариации нет,
     * возвращает ссылку на вариацию по умолчанию. В случае, когда [variations] пуст,
     * выбрасывает исключение.
     *
     * @param key ключ запрашиваемой вариации стиля
     * @return ссылка на стиль [SR] для указанной вариации или вариации по умолчанию
     *
     * @throws IllegalStateException если [variations] пуст (не зарегистрировано ни одной вариации)
     *
     * @see defaultVariant
     */
    fun styleReference(key: String): SR = variations[key]
        ?: variations[defaultVariant]
        ?: throw IllegalStateException("Style $key not found")
}
